
package bean;

public class ConfirmPresencePatientBean {
    
     private String adresseMail;
    private String password;
    private String role;
    private String validePresence;
    private String numeroNational;

    public String getAdresseMail() {
        return adresseMail;
    }

    public void setAdresseMail(String adresseMail) {
        this.adresseMail = adresseMail;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getValidePresence() {
        return validePresence;
    }

    public void setValidePresence(String validePresence) {
        this.validePresence = validePresence;
    }

    public String getNumeroNational() {
        return numeroNational;
    }

    public void setNumeroNational(String numeroNational) {
        this.numeroNational = numeroNational;
    }
    
    
    
    
}
